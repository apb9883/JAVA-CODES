public class Loop {
    public static void main(String[] args) {
        int a=1;                                // initialization
        while(a<=100){                          // condition
            System.out.print(a+" ");
            a=a+1;                              // increement
        }
        System.out.println();
        int count=500;
        while(count>=200){
            System.out.print(count+" ");
            count=count-1;
        }
    }
}
